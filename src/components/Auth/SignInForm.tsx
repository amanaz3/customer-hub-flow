import React, { useState } from 'react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Eye, EyeOff } from 'lucide-react';
import { useAuth } from '@/contexts/SecureAuthContext';
import { useToast } from '@/hooks/use-toast';

interface SignInFormProps {
  isLoading: boolean;
  setIsLoading: (loading: boolean) => void;
}

const SignInForm: React.FC<SignInFormProps> = ({ isLoading, setIsLoading }) => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const { signIn } = useAuth();
  const { toast } = useToast();

  const handleSignIn = async (e: React.FormEvent) => {
    e.preventDefault();
    
    if (!email || !password) {
      toast({
        title: 'Error',
        description: 'Please fill in all fields',
        variant: 'destructive',
      });
      return;
    }

    setIsLoading(true);
    
    try {
      console.log('Attempting sign in for:', email);
      const { error } = await signIn(email, password);
      
      if (error) {
        console.error('Sign in failed:', error);
        
        // Check for CORS-related errors
        const isCorsError = error.name === 'TypeError' && 
          (error.message?.includes('fetch') || error.message?.includes('network')) ||
          error.status === 0;
          
        if (isCorsError) {
          toast({
            title: 'Connection Error',
            description: 'Origin not allowed (CORS). Please contact your administrator to whitelist this domain.',
            variant: 'destructive',
          });
        } else {
          toast({
            title: 'Sign In Failed',
            description: error.message || 'Invalid email or password',
            variant: 'destructive',
          });
        }
      } else {
        console.log('Sign in successful');
        toast({
          title: 'Welcome back!',
          description: 'You have been signed in successfully.',
        });
        // Navigation will be handled by the auth state change
      }
    } catch (error: any) {
      console.error('Sign in error:', error);
      
      // Check for CORS-related network errors
      const isCorsError = error.name === 'TypeError' || 
        error.message?.includes('fetch') || 
        error.message?.includes('network') ||
        error.message?.includes('Failed to fetch');
        
      if (isCorsError) {
        toast({
          title: 'Connection Error',
          description: 'Origin not allowed (CORS). Please contact your administrator to whitelist this domain.',
          variant: 'destructive',
        });
      } else {
        toast({
          title: 'Error', 
          description: 'An unexpected error occurred',
          variant: 'destructive',
        });
      }
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <form onSubmit={handleSignIn} className="space-y-4">
      <div className="space-y-2">
        <Label htmlFor="signin-email">Email</Label>
        <Input
          id="signin-email"
          type="email"
          placeholder="name@company.com"
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          disabled={isLoading}
          required
        />
      </div>
      <div className="space-y-2">
        <Label htmlFor="signin-password">Password</Label>
        <div className="relative">
          <Input
            id="signin-password"
            type={showPassword ? 'text' : 'password'}
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            disabled={isLoading}
            required
          />
          <Button
            type="button"
            variant="ghost"
            size="sm"
            className="absolute right-0 top-0 h-full px-3 py-2 hover:bg-transparent"
            onClick={() => setShowPassword(!showPassword)}
            disabled={isLoading}
          >
            {showPassword ? <EyeOff className="h-4 w-4" /> : <Eye className="h-4 w-4" />}
          </Button>
        </div>
      </div>
      <Button type="submit" className="w-full" disabled={isLoading}>
        {isLoading ? 'Signing in...' : 'Sign In'}
      </Button>
    </form>
  );
};

export default SignInForm;